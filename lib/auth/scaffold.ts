/**
 * B1: Auth Scaffold — code-level companions to the Auth Providers panel.
 *
 * Drops a production-grade auth surface into the user's repo:
 *   - app/auth/callback/route.ts            — handles OAuth + magic link redirect
 *   - app/auth/signout/route.ts             — POST signout endpoint
 *   - lib/auth/server.ts                    — typed createClient for RSC + routes
 *   - lib/auth/client.ts                    — typed createClient for browser
 *   - middleware.ts                         — session refresh + protected routes
 *   - lib/auth/jwt-hook-template.sql        — opt-in Postgres function for custom JWT claims
 *   - app/(auth)/mfa/page.tsx               — opt-in TOTP enroll/verify UI
 *
 * Each piece is independently togglable. Lovable hides all of this — devs
 * want the actual files so they can extend, audit, and own the auth surface.
 */

export interface AuthScaffoldConfig {
  /** Frameworks the scaffold targets — currently only Next.js App Router */
  framework?: 'next-app'
  /** Include MFA enrollment + verify components */
  includeMfa?: boolean
  /** Include custom JWT claims template (Postgres function) */
  includeJwtHook?: boolean
  /** Comma-separated list of protected route patterns (for middleware) */
  protectedRoutes?: string[]
}

export interface GeneratedFile {
  path:     string
  content:  string
  byteSize: number
}

// ─── Public entry point ─────────────────────────────────────────────────────

export function generateAuthScaffold(cfg: AuthScaffoldConfig = {}): GeneratedFile[] {
  const files: GeneratedFile[] = [
    mk('app/auth/callback/route.ts',  buildCallback()),
    mk('app/auth/signout/route.ts',   buildSignout()),
    mk('lib/auth/server.ts',          buildServerClient()),
    mk('lib/auth/client.ts',          buildBrowserClient()),
    mk('middleware.ts',               buildMiddleware(cfg.protectedRoutes ?? ['/dashboard', '/settings'])),
  ]
  if (cfg.includeMfa) {
    files.push(mk('app/(auth)/mfa/page.tsx',           buildMfaPage()))
    files.push(mk('app/(auth)/mfa/enroll-action.ts',   buildMfaEnrollAction()))
  }
  if (cfg.includeJwtHook) {
    files.push(mk('supabase/migrations/' + tsName() + '_custom_access_token_hook.sql', buildJwtHookSql()))
  }
  return files
}

function mk(path: string, content: string): GeneratedFile {
  return { path, content, byteSize: Buffer.byteLength(content, 'utf8') }
}

function tsName(): string {
  return new Date().toISOString().slice(0, 19).replace(/[-T:]/g, '')
}

