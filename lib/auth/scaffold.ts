function buildMiddleware(protectedPatterns: string[]): string {
  const matcherJs = JSON.stringify(protectedPatterns.map((p) => `${p}/:path*`))
  return `// Generated by SpringBloom — refreshes the user's session on every request,
// and bounces unauthenticated visitors away from protected routes.

import { type NextRequest, NextResponse } from 'next/server'
import { createServerClient } from '@supabase/ssr'

const PROTECTED = ${JSON.stringify(protectedPatterns)}

export async function middleware(request: NextRequest) {
  let response = NextResponse.next({ request })

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() { return request.cookies.getAll() },
        setAll(toSet) {
          toSet.forEach(({ name, value, options }) => {
            request.cookies.set(name, value)
            response = NextResponse.next({ request })
            response.cookies.set(name, value, options)
          })
        },
      },
    },
  )

  // IMPORTANT: getUser() refreshes the session if the access token is stale.
  const { data: { user } } = await supabase.auth.getUser()

  // Redirect unauthenticated users to /login on protected routes
  const path = request.nextUrl.pathname
  const isProtected = PROTECTED.some((p) => path === p || path.startsWith(p + '/'))
  if (!user && isProtected) {
    const url = request.nextUrl.clone()
    url.pathname = '/login'
    url.searchParams.set('next', path)
    return NextResponse.redirect(url)
  }

  return response
}

export const config = {
  // Match everything except static assets + Next internals.
  matcher: [
    '/((?!_next/static|_next/image|favicon.ico|.*\\\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)',
  ],
}

// Matcher patterns above run middleware globally; we filter to PROTECTED inside.
// You can also restrict matcher itself to: ${matcherJs}
`
}

function buildMfaPage(): string {
  return `// Generated by SpringBloom — TOTP MFA enroll & verify (Supabase Auth).

"use client"

import { useEffect, useState } from "react"
import { createClient } from "@/lib/auth/client"
import { enrollMfaAction } from "./enroll-action"

export default function MfaPage() {
  const [qr, setQr]           = useState<string | null>(null)
  const [secret, setSecret]   = useState<string | null>(null)
  const [factorId, setFactor] = useState<string | null>(null)
  const [code, setCode]       = useState("")
  const [status, setStatus]   = useState<"idle" | "enrolling" | "verifying" | "verified" | "error">("idle")
  const [error, setError]     = useState<string | null>(null)

  async function enroll() {
    setStatus("enrolling"); setError(null)
    try {
      const res = await enrollMfaAction()
      setQr(res.qr)
      setSecret(res.secret)
      setFactor(res.factorId)
      setStatus("idle")
    } catch (e) {
      setError(e instanceof Error ? e.message : "Enroll failed")
      setStatus("error")
    }
  }

  async function verify() {
    if (!factorId) return
    setStatus("verifying"); setError(null)
    const supabase = createClient()
    const { data: challenge } = await supabase.auth.mfa.challenge({ factorId })
    if (!challenge) { setError("Could not challenge factor"); setStatus("error"); return }
    const { error: verifyErr } = await supabase.auth.mfa.verify({
      factorId, challengeId: challenge.id, code,
    })
    if (verifyErr) { setError(verifyErr.message); setStatus("error"); return }
    setStatus("verified")
  }

  useEffect(() => { /* could auto-enroll on mount */ }, [])

  return (
    <div className="mx-auto max-w-md p-6 space-y-4">
      <h1 className="text-xl font-semibold">Two-factor authentication</h1>
      {!qr && (
        <button onClick={enroll} className="px-3 py-2 rounded bg-violet-600 text-white">
          {status === "enrolling" ? "Enrolling..." : "Enable TOTP"}
        </button>
      )}
      {qr && status !== "verified" && (
        <div className="space-y-3">
          <p className="text-sm">Scan with Authy / Google Authenticator / 1Password:</p>
          <img src={qr} alt="MFA QR" className="border" />
          <p className="text-xs text-zinc-500">Or enter the secret: <code className="font-mono">{secret}</code></p>
          <input
            value={code} onChange={(e) => setCode(e.target.value)}
            placeholder="6-digit code" maxLength={6}
            className="border rounded px-2 py-1 font-mono w-32"
          />
          <button onClick={verify} disabled={code.length !== 6} className="px-3 py-2 rounded bg-emerald-600 text-white disabled:opacity-50">
            {status === "verifying" ? "Verifying..." : "Verify"}
          </button>
        </div>
      )}
      {status === "verified" && <p className="text-emerald-600 font-semibold">✓ MFA enabled</p>}
      {error && <p className="text-red-600 text-sm">{error}</p>}
    </div>
  )
}
`
}

function buildMfaEnrollAction(): string {
  return `// Generated by SpringBloom — server action: enroll a TOTP factor.

"use server"

import { createServerClient } from "@/lib/auth/server"

export async function enrollMfaAction(): Promise<{ qr: string; secret: string; factorId: string }> {
  const supabase = await createServerClient()
  const { data, error } = await supabase.auth.mfa.enroll({
    factorType: "totp",
    friendlyName: "Authenticator",
  })
  if (error || !data) throw new Error(error?.message ?? "Enroll failed")
  return {
    qr:       data.totp.qr_code,
    secret:   data.totp.secret,
    factorId: data.id,
  }
}
`
}

