function buildCallback(): string {
  return `// Generated by SpringBloom — OAuth + magic-link callback handler.
// Reached after a successful sign-in via Google/Apple/Facebook/GitHub OR
// after the user clicks the magic-link sent to their email.

import { NextResponse } from 'next/server'
import { createServerClient } from '@/lib/auth/server'

export async function GET(req: Request) {
  const url = new URL(req.url)
  const code = url.searchParams.get('code')
  // Where to redirect after auth completes. Defaults to /dashboard.
  const next = url.searchParams.get('next') ?? '/dashboard'

  if (!code) {
    return NextResponse.redirect(\`\${url.origin}/login?error=missing_code\`)
  }

  const supabase = await createServerClient()
  const { error } = await supabase.auth.exchangeCodeForSession(code)
  if (error) {
    console.error('[auth/callback] exchange failed:', error.message)
    return NextResponse.redirect(\`\${url.origin}/login?error=\${encodeURIComponent(error.message)}\`)
  }

  // TODO (optional): run first-login hooks here.
  //   - create a profiles row
  //   - assign default plan
  //   - send a welcome email

  return NextResponse.redirect(\`\${url.origin}\${next}\`)
}
`
}

function buildSignout(): string {
  return `// Generated by SpringBloom — signout endpoint.
// POST here from your nav menu / settings button.

import { NextResponse } from 'next/server'
import { createServerClient } from '@/lib/auth/server'

export async function POST(req: Request) {
  const supabase = await createServerClient()
  await supabase.auth.signOut()
  const origin = new URL(req.url).origin
  return NextResponse.redirect(\`\${origin}/\`, { status: 303 })
}
`
}

function buildServerClient(): string {
  return `// Generated by SpringBloom — Supabase server client for Server Components + Route Handlers.
// SSR-safe: reads/writes cookies via Next.js cookies() API so the session
// stays consistent across server renders.

import { createServerClient as _create } from '@supabase/ssr'
import { cookies } from 'next/headers'

export async function createServerClient() {
  const cookieStore = await cookies()
  return _create(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() { return cookieStore.getAll() },
        setAll(toSet) {
          try { toSet.forEach(({ name, value, options }) => cookieStore.set(name, value, options)) }
          catch { /* called from a Server Component — middleware handles refresh */ }
        },
      },
    },
  )
}
`
}

