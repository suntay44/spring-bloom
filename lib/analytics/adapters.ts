/**
 * BYO-Analytics adapters.
 *
 * Lovable forces their built-in dashboard. We let devs use the tool they
 * already love by emitting one small snippet into the project repo.
 *
 * Each adapter exposes:
 *   - kind: stable identifier for the integration row
 *   - label: human-facing name
 *   - fields: config fields the user fills in (project ID, host, etc.)
 *   - filePath: where the snippet lives in the project
 *   - buildSnippet(config): returns the file contents
 *
 * Snippets are intentionally framework-agnostic head-snippets where possible
 * (work in Next.js, Remix, TanStack Start, Vite + React Router). The user
 * imports them in their root layout.
 */

export type AnalyticsKind = 'posthog' | 'plausible' | 'umami' | 'ga4'

export interface AdapterField {
  key:         string
  label:       string
  placeholder?: string
  hint?:        string
  required?:    boolean
}

export interface AnalyticsAdapter {
  kind:        AnalyticsKind
  label:       string
  description: string
  homepage:    string
  pricing:     string                          // short marketing line
  fields:      AdapterField[]
  filePath:    string                          // where to write the snippet
  buildSnippet: (config: Record<string, string>) => string
}

