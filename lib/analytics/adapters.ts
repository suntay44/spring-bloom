// ─── Google Analytics 4 ─────────────────────────────────────────────────────

const ga4: AnalyticsAdapter = {
  kind: 'ga4',
  label: 'Google Analytics 4',
  description: 'The default for free analytics. Cookie consent often required.',
  homepage: 'https://analytics.google.com',
  pricing: 'Free up to 10M events/mo',
  fields: [
    { key: 'measurement_id', label: 'Measurement ID', placeholder: 'G-XXXXXXXXXX', required: true },
  ],
  filePath: 'components/analytics/GAScript.tsx',
  buildSnippet: (cfg) => `// Generated by SpringBloom — BYO Analytics (Google Analytics 4)
// NOTE: most jurisdictions require a cookie consent banner before loading GA.
//       Gate this script on consent.

import Script from 'next/script'

const MEASUREMENT_ID = ${JSON.stringify(cfg.measurement_id ?? '')}

export function GAScript() {
  if (!MEASUREMENT_ID) return null
  return (
    <>
      <Script
        src={\`https://www.googletagmanager.com/gtag/js?id=\${MEASUREMENT_ID}\`}
        strategy="afterInteractive"
      />
      <Script id="ga4-init" strategy="afterInteractive">{\`
        window.dataLayer = window.dataLayer || [];
        function gtag(){dataLayer.push(arguments);}
        gtag('js', new Date());
        gtag('config', '\${MEASUREMENT_ID}', { send_page_view: true });
      \`}</Script>
    </>
  )
}
`,
}

